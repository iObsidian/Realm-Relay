package flash.geom;

public class Rectangle {
	public double x;
	public double y;

	public double width;
	public double height;
	public double right;
	public double bottom;

	public Rectangle(double x, double y, double width, double height) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
}
